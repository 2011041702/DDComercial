public class Combos {
    private String id_combo;
    private String nom_comb;
    private String desc_comb;
    private String precio_comb;
    private String img_comb;

    public String getId_combo() {
        return id_combo;
    }

    public void setId_combo(String id_combo) {
        this.id_combo = id_combo;
    }

    public String getNom_comb() {
        return nom_comb;
    }

    public void setNom_comb(String nom_comb) {
        this.nom_comb = nom_comb;
    }

    public String getDesc_comb() {
        return desc_comb;
    }

    public void setDesc_comb(String desc_comb) {
        this.desc_comb = desc_comb;
    }

    public String getPrecio_comb() {
        return precio_comb;
    }

    public void setPrecio_comb(String precio_comb) {
        this.precio_comb = precio_comb;
    }

    public String getImg_comb() {
        return img_comb;
    }

    public void setImg_comb(String img_comb) {
        this.img_comb = img_comb;
    }
}
